// Compare Two Strings

public class Compare_Two_Strings {
    public static void main(String[] args) {
        String str1 = "Hello";
        String str2 = "Hello";
        String str3 = "World";

        System.out.println(str1.equals(str2)); // true
        System.out.println(str1.equals(str3)); // false
    }

}


/*
Output:
true
false
 */